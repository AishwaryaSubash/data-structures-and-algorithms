#include <bits/stdc++.h>
using namespace std;

int main() {
    int n;
    cout<<"rows :";
    cin>>n;
    
    for (int i=1 ; i<=2*n ; i++) {
        for (int j=1 ; j<=2*n ; j++) {
            cout<<"*";
            
        }
        cout<<"\n";
    }
    return 0;
}


